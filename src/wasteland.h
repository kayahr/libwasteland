/**
 * @mainpage libwasteland API Documentation
 *
 * @section description Description
 *
 * libwasteland is a C++ shared library which can be used to read and write
 * the data files of the classic role playing game 
 * <a href="http://en.wikipedia.org/wiki/Wasteland_(video_game)">Wasteland</a>.
 *
 * Implemented features:
 *
 * <ul>
 *   <li>
 *     Reading and writing the title picture (title.pic). 
 *     See {@link wasteland::title_pic title_pic} class.
 *   </li>
 *   <li>
 *     Reading and writing the sprites (ic0_9.wlf and masks.wlf).
 *     See {@link wasteland::sprite sprite} class.
 *   </li>
 * </ul>
 */